/**
 * Get test texts for a specific language
 * 
 * @param {string} language - Language code
 * @returns {Object} Test texts object with short, medium, long
 */
export function getTestTexts(language = null) {
    if (language) {
        return TEST_TEXTS[language] || TEST_TEXTS.english;
    }
    return TEST_TEXTS;
}

/**
 * Get a specific test text
 * 
 * @param {string} language - Language code
 * @param {string} length - Text length ('short', 'medium', 'long')
 * @returns {string} Test text
 */
export function getTestText(language, length = 'medium') {
    const texts = getTestTexts(language);
    return texts[length] || texts.medium;
}

